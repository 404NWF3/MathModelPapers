function[a1,a2,a3]=Ideal_Cosine_theorem(x,y)
%% 该函数用于求解理想坐标系三个角度的大小
L=50;
e1=(x-0)^2+(y-4*L)^2+x^2+y^2-(4*L)^2;
e2=2*sqrt(x^2+(y-4*L)^2)*sqrt(x^2+y^2);
e3=(x-0)^2+(y-4*L)^2+(x-3.464*L)^2+(y-2*L)^2-((3.464*L)^2+(2*L)^2);
e4=2*sqrt(x^2+(y-4*L)^2)*sqrt((x-3.464*L)^2+(y-2*L)^2);
e5=(x)^2+(y)^2+(x-3.464*L)^2+(y-2*L)^2-((3.464*L)^2+(2*L)^2);
e6=2*sqrt(x^2+(y)^2)*sqrt((x-3.464*L)^2+(y-2*L)^2);
a1=acos(e1/e2)/pi*180;
a2=acos(e3/e4)/pi*180;
a3=acos(e5/e6)/pi*180;
end